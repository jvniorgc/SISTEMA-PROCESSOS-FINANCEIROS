    public void showBankAccountUser() {
        ArrayList<DataBankAccount> lista = new ArrayList<>();
        try {
            GestManegmentDataUser GMDU = new GestManegmentDataUser();
            //receber o retorno do método que está em ControleContato
            DataUser DU = new DataUser();
            DU.setIdUser(id);
            lista = GMDU.listBankingAccount(DU);
            //limpar a interface da tabela
            user.setNumRows(0);//set zero linhas
            //percorrer a tabela na interface para colocar os registros que resultou da query(SELECT)
            for (int i = 0; i < lista.size(); i++) {
                user.addRow(
                        new String[]{
                            Integer.toString(lista.get(i).getIdAccount()),
                            Float.toString(lista.get(i).getBalance()),
                            Integer.toString(lista.get(i).getBankId()),
                            Integer.toString(lista.get(i).getUserId())
                        }
                //string é uma classe, então precisa instanciar a cada nova volta
                );
            }
            tableDataContact1.setModel(user);
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro na tabela.\nCausa: " + erro.getMessage(), "Erro", 0);
        }
    }

