    private void btnAtualizar1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAtualizar1MouseClicked
        // TODO add your handling code here:
        
        try {
            
            //pegar o retorno da linha selecionada na interface
            int linha = tableDataContact1.getSelectedRow();
            System.out.println(""+tableDataContact1.getSelectedRow());
            //se a linha foi selecionada
            if (linha != -1) {
                //pegar o valor através(PK) da referência de linha e coluna
                String codigo = (String) tableDataContact1.getValueAt(linha, 0);
                DataBankAccount DBA = new DataBankAccount();
                DataTransaction DT = new DataTransaction();
                //converter string para inteiro
                DBA.setIdAccount(Integer.parseInt(codigo));
                DT.setStartAccount(Integer.parseInt(codigo));
                GestManegmentDataBankAccount GMDBA = new GestManegmentDataBankAccount();
                GMDBA.deleteBankAccount(DBA, DT);

                //atualizar tabela depois de deletar
                showBankAccountUser();

                //mensagem
                JOptionPane.showMessageDialog(null, "Conta deleta.", "SUCESSO", 1);

//atualizar a tabela depois de deletar
            } else {
                throw new SQLDataException("Selecione uma linha.");
                
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro ao apagar conta.\nCausa: " + erro.getMessage(), "Erro", 0);
        }
    }//GEN-LAST:event_btnAtualizar1MouseClicked


